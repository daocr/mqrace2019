public class TA {
    long t;
    long a;

    public TA(long t, long a) {
        this.t = t;
        this.a = a;
    }

    public long getT() {
        return t;
    }

    public void setT(long t) {
        this.t = t;
    }

    public long getA() {
        return a;
    }

    public void setA(long a) {
        this.a = a;
    }
}
